package week3day1;

public class ForLoopForward {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            //변수 값은 0에서 시작(변수시작값); i가 10보다 작을 때 까지만 반복(반복 조건); i를 하나씩 올림(증감식, 변수의 변화)

                System.out.println(i); //i가 0~9까지 변함
            }
        }
    }
