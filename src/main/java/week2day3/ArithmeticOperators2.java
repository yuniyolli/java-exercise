package week2day3;

public class ArithmeticOperators2 {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        System.out.println(10 + b);
        System.out.println(a - 3);
        System.out.println(a * b);
        System.out.println(10 * 3 / b);
        System.out.println(49 % b);
    }
}
