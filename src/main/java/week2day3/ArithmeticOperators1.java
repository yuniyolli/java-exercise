package week2day3;

public class ArithmeticOperators1 {
    public static void main(String[] args) {
        System.out.printf("a + b = %d\n", 3 + 10);
        System.out.printf("a - b = %d\n", 3 - 10);
        System.out.printf("a * b = %d\n", 3 * 10);
        System.out.printf("a / b = %d\n", 10 / 3);
        System.out.printf("a %% b = %d\n", 10 % 3);

        //%하나만하면 %d이런거랑 헷갈릴수있음 두번씩 쓰는 습관
        //근데 ""안에서만! souf밖에서는 한번이 맞군
        //매번 헷갈리는 식넣기!!!! 안에는 그냥 정수값이라는거만 적고 식은 밖에 연산자식 만들면되
    }
}
