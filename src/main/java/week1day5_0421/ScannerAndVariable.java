package week1day5_0421;

import java.util.Scanner;

public class ScannerAndVariable {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        //변수 선언 + sc.nextInt()로 입력받은 값으로 초기화
        int iVal = sc.nextInt();
        //iVal 출력
        System.out.println(iVal);
    }
}
